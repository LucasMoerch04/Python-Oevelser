#Moduler og pakker er begge måder at organisere kode på i Python. Et modul er en enkelt fil, der indeholder Python-kode, mens en pakke er en samling af moduler, der kan indeholde andre pakker og undermapper.

#"import" er en Python-nøgleord, der bruges til at importere moduler eller pakker i en anden fil. "from X import y" betyder, at kun en bestemt funktion eller klasse fra modulet X vil blive importeret og er tilgængelig i den nuværende fil.

#"Pip" er en pakkeinstallationsprogram til Python. Det bruges til at installere, administrere og afinstallere pakker, der ikke er en del af standardbiblioteket. Det er tilgængeligt på Python Package Index (PyPI) og kan tilgås via kommandolinjen eller terminalen.


